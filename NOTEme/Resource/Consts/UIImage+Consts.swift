import UIKit

extension UIImage {
    
    //MARK: - General
    enum General {
        static let logo: UIImage = .init(named: "logo")!
    }
    //MARK: - Onboarding
    enum Onboarding {
        static let step: UIImage = .init(named: "Step")!
    }
    enum TabBar {
        static let home: UIImage = .init(named: "home")!
        static let profile: UIImage = .init(named: "profile")!
    }
}
